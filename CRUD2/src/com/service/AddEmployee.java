package com.service;

import com.model.EmployeModel;

public class AddEmployee {
	public static EmployeModel[] addEmployee(EmployeModel[] employees, EmployeModel employee) {
		int count = employees.length;
		EmployeModel[] employees1 = new EmployeModel[count+1];
		for (int i = 0; i < count; i++) {
			employees1[i] = employees[i];
			
		}
		employees1[count] = employee;
		System.out.println(employees1.length);
		return employees1;
	}
}
