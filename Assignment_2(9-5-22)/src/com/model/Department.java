package com.model;

public class Department {
	private String detName;
	private int depId;
	private int depSalary;
	private int depAge;
	public String getDetName() {
		return detName;
	}
	public void setDetName(String detName) {
		this.detName = detName;
	}
	public int getDepId() {
		return depId;
	}
	public void setDepId(int depId) {
		this.depId = depId;
	}
	public int getDepSalary() {
		return depSalary;
	}
	public void setDepSalary(int depSalary) {
		this.depSalary = depSalary;
	}
	public int getDepAge() {
		return depAge;
	}
	public void setDepAge(int depAge) {
		this.depAge = depAge;
	}
	

}
